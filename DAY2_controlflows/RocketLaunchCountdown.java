public class RocketLaunchCountdown {
    public static void main(String[] args) {
        // Countdown from 10 to 1
        for (int i = 10; i > 0; i--) {
            System.out.println(i);
        }
        
        // Print "Liftoff!" when the countdown reaches zero
        System.out.println("Liftoff!");
    }
    
}
