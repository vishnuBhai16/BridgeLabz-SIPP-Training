public class MultiplesBelow100 {
    public static void main(String[] args) {
        // Print multiples of 5 below 100
        System.out.println("Multiples of 5 below 100:");
        for (int i = 5; i < 100; i += 5) {
            System.out.println(i);
        }

        // Print multiples of 7 below 100
        System.out.println("Multiples of 7 below 100:");
        for (int i = 7; i < 100; i += 7) {
            System.out.println(i);
        }
    }
    
}
