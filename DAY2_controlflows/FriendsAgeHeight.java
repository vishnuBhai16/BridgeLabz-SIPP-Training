public class FriendsAgeHeight {
    public static void main(String[] args) {
        // Define the ages and heights of friends
        int age1 = 25, age2 = 30, age3 = 28;
        double height1 = 5.6, height2 = 5.8, height3 = 5.7;

        // Calculate the average age
        double averageAge = (age1 + age2 + age3) / 3.0;

        // Calculate the average height
        double averageHeight = (height1 + height2 + height3) / 3.0;

        // Print the results
        System.out.println("Average Age: " + averageAge);
        System.out.println("Average Height: " + averageHeight);
    }
    
}
